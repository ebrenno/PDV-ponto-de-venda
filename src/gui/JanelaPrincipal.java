    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int codProd = listaProduto.get( jComboBox3.getSelectedIndex() ).getCodProd();
        int codCli  = listaCliente.get( jComboBox1.getSelectedIndex() ).getCodCli();
        int codLocal= listaLocalidade.get( jComboBox2.getSelectedIndex() ).getCodLocal();
        int quantidade;
        try{
   
           quantidade =  Integer.parseInt( jTextField1.getText() );
           if(quantidade <= 0){
                JOptionPane.showMessageDialog(null, "Informe a quantidade correta!","Alerta!",JOptionPane.WARNING_MESSAGE);
                return;               
           }
           
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Informe uma quantidade válida!","Erro!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection conexao;
        try {
            conexao = DatabaseFactory.getConn(DatabasesURL.MY_SQL);
            VendaDAO.aplicar(conexao, codProd , codCli , codLocal , quantidade );
            JOptionPane.showMessageDialog(null, "venda concluída com sucesso!");
            DatabaseFactory.fecharConexao(conexao);
        } catch (SQLIntegrityConstraintViolationException ex) {
            JOptionPane.showMessageDialog(null, "Esta venda já está cadastrada!", "Erro!", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na gravação da venda!", "Erro!", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch(EstoqueInsuficienteException ex){
            JOptionPane.showMessageDialog(null, "Produto sem estoque", "Erro!", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        preencherTabela();
        
    }//GEN-LAST:event_jButton1ActionPerformed

