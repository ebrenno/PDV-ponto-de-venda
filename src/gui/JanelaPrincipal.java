    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int index = jTable1.getSelectedRow();
            if(index == -1){
                JOptionPane.showMessageDialog(null, "nenhuma venda selecionada", "Erro!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int codCli = listaCliente.get( jComboBox1.getSelectedIndex() ).getCodCli();
            Connection conexao = DatabaseFactory.getConn(DatabasesURL.MY_SQL);
            List<Venda> vendas = VendaDAO.recuperarVendas(conexao, codCli);
            Venda venda = vendas.get(index);
            VendaDAO.excluir(conexao, venda);
            preencherTabela();
            DatabaseFactory.fecharConexao(conexao);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "serviço indisponível", "Erro!", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

