    private void initComboBox(){
       
        try {
            Connection conexao;
            conexao         = DatabaseFactory.getConn(DatabasesURL.MY_SQL);
            listaCliente    = ClienteDAO.listaCliente(conexao);
            listaLocalidade = LocalidadeDAO.listaLocalidade(conexao);
            listaProduto    = ProdutoDAO.listaProduto(conexao);

            vetorCliente    = preencherCliente(listaCliente);
            vetorLocalidade = preencherLocalidade(listaLocalidade);
            vetorProduto    = preencherProduto(listaProduto);

            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>( vetorCliente ));
            jComboBox2.setModel(new javax.swing.DefaultComboBoxModel<>( vetorLocalidade ));
            jComboBox3.setModel(new javax.swing.DefaultComboBoxModel<>( vetorProduto ));
            jTextField2.setEditable(false);
            jTextField2.setText( listaProduto.get(0).getDescricao() );
            DatabaseFactory.fecharConexao(conexao);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "serviço indisponível", "Erro!", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private static Object[] preencherCliente(List<Cliente> lista){

        return lista.stream().map(cliente->cliente.getNome()).toArray();
  
    }
    private static Object[] preencherLocalidade(List<Localidade> lista){

        return lista.stream().map(local->local.getNome()).toArray();
    }

    private static Object[] preencherProduto(List<Produto> lista){

        return lista.stream().map(produto->produto.getCodProd()).toArray();
    }

    public void preencherTabela(){
        int codCli  = listaCliente.get( jComboBox1.getSelectedIndex() ).getCodCli();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        Connection conexao;
        List<Tabela> lista = new ArrayList<>();
        try {
            conexao = DatabaseFactory.getConn(DatabasesURL.MY_SQL);
            lista = VendaDAO.recuperarItensVendidos(conexao,codCli);
            DatabaseFactory.fecharConexao(conexao);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(JanelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        double totalPagar = 0;
        for(Tabela tupla : lista){
            modelo.addRow(new Object[]{ tupla.getDescricao() , tupla.getQtdVenda() , String.format("%.2f", tupla.getPrecoUnitario() ) , String.format("%.2f", tupla.getValorTotal() ) });
            totalPagar += tupla.getValorTotal();
        }
        
        String totalFormatada =  String.format("%.2f", totalPagar );
      
        jTextField3.setText( totalFormatada );
    
    }
    
